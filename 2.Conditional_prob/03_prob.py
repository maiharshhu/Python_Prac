if stud_score>100:
    print("You Enter wrong marks")

else:
    if stud_score>=90:
        print("A")

    elif stud_score>=80:
        print("B")

    elif stud_score>=70:
        print("C")

    elif stud_score>=60:
        print("D")

    else:
        print("F")

